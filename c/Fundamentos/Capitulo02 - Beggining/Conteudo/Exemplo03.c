// Escreva um programa que diga a quantidade de bytes que o ocupa um inteiro
#include <stdio.h>

int main(void){
    printf("O tamanho em bytes de um inteiro e: %d\n", sizeof(int));
    printf("O tamanho em bytes de um char e: %d\n", sizeof(char));
    printf("O tamanho em bytes de um float e: %d\n", sizeof(float));
    printf("O tamanho em bytes de um double e: %d\n", sizeof(double));
}